/**
 * Returns the IQ interpretation object for a given score.
 * @param {number} score
 * @returns {{ label: string, description: string }}
 */
function getIQLevel(score) {
    return IQ_LEVELS.find(l => score >= l.minScore) || IQ_LEVELS[IQ_LEVELS.length - 1];
}

/**
 * Returns IQ and EQ advice arrays for the given scores.
 * @param {number} iqScore
 * @param {number} eqScore
 * @returns {{ iqAdvice: string[], eqAdvice: string[] }}
 */
function getAdvice(iqScore, eqScore) {
    const iq = IQ_ADVICE.find(a => iqScore <= a.maxScore);
    const eq = EQ_ADVICE.find(a => eqScore <= a.maxScore);
    return {
        iqAdvice: iq ? iq.items : [],
        eqAdvice: eq ? eq.items : []
    };
}

/**
 * Returns the emoji icon for a personality type.
 * @param {string} type
 * @returns {string}
 */
function getPersonalityIcon(type) {
    return PERSONALITY_ICONS[type] || PERSONALITY_ICONS.default;
}
